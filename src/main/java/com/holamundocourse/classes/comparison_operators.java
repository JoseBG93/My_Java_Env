package com.holamundocourse.classes;

public class comparison_operators {
    public static void main(String[] args) {
        int x = 15;
        int y = 20;
        System.out.println(x == y);
        System.out.println(x != y);
        System.out.println(x > y);
        System.out.println(x < y);
    }
}